class YearModel {
  final int month;
  final int year;
  final int saldoInicial;
  final int saldoVentas;
  final int saldoGastos;
  final int saldoGanancias;

  YearModel({
    required this.month,
    required this.year,
    required this.saldoInicial,
    required this.saldoVentas,
    required this.saldoGastos,
    required this.saldoGanancias,
  });
}
